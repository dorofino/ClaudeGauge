class SerialMonitor:
    """Background serial port reader with line buffering."""

    def __init__(self):
        self._proc = None
        self._serial = None
        self._lines = []
        self._lock = threading.Lock()
        self._connected = False
        self._port = ""

    def connect(self, port: str, baud: int = 115200) -> bool:
        self.disconnect()
        self._port = port
        self._lines = [f"--- Connecting to {port} at {baud} baud ---"]

        # Try pyserial first, fall back to PlatformIO device monitor
        try:
            import serial as pyserial
            self._serial = pyserial.Serial(port, baud, timeout=0.1)
            # Toggle DTR to reset the ESP32 so we see boot messages
            self._serial.dtr = False
            time.sleep(0.1)
            self._serial.dtr = True
            self._connected = True
            thread = threading.Thread(target=self._pyserial_reader, daemon=True)
            thread.start()
            print(f"  Serial connected (pyserial): {port}")
            return True
        except ImportError:
            print("  pyserial not available, trying PlatformIO...")
        except Exception as e:
            self._lines.append(f"ERROR: {e}")
            print(f"  Serial error: {e}")
            return False

        # Fallback: PlatformIO device monitor
        try:
            cmd = [PIO_CMD, "device", "monitor",
                   "--port", port, "--baud", str(baud),
                   "--no-reconnect", "--raw"]
            self._proc = subprocess.Popen(
                cmd, stdout=subprocess.PIPE, stderr=subprocess.STDOUT,
                text=True, bufsize=1
            )
            self._connected = True
            thread = threading.Thread(target=self._pio_reader, daemon=True)
            thread.start()
            print(f"  Serial connected (pio): {port}")
            return True
        except Exception as e:
            self._lines.append(f"ERROR: {e}")
            print(f"  Serial fallback error: {e}")
            return False

    def _pyserial_reader(self):
        """Read from pyserial directly."""
        buf = ""
        try:
            while self._connected and self._serial and self._serial.is_open:
                data = self._serial.read(256)
                if data:
                    buf += data.decode("utf-8", errors="replace")
                    while "\n" in buf:
                        line, buf = buf.split("\n", 1)
                        with self._lock:
                            self._lines.append(line.rstrip())
                else:
                    time.sleep(0.05)
        except Exception:
            pass
        finally:
            with self._lock:
                self._connected = False
                self._lines.append("--- Disconnected ---")

    def _pio_reader(self):
        """Read from PlatformIO device monitor subprocess."""
        try:
            for line in self._proc.stdout:
                with self._lock:
                    self._lines.append(line.rstrip())
        except Exception:
            pass
        finally:
            with self._lock:
                self._connected = False
                self._lines.append("--- Disconnected ---")

    def disconnect(self):
        self._connected = False
        if self._serial:
            try:
                self._serial.close()
            except Exception:
                pass
            self._serial = None
        if self._proc:
            try:
                self._proc.terminate()
                self._proc.wait(timeout=3)
            except Exception:
                try:
                    self._proc.kill()
                except Exception:
                    pass
            self._proc = None

    def get_lines(self, from_line: int = 0) -> dict:
        with self._lock:
            return {
                "lines": self._lines[from_line:],
                "connected": self._connected,
                "port": self._port,
                "total": len(self._lines)
            }

    @property
    def is_connected(self):
        return self._connected



def find_pio():
    """Find the pio executable."""
    # Try PATH first
    import shutil
    pio = shutil.which("pio")
    if pio:
        return pio
    # Windows fallback
    win_path = Path.home() / ".platformio" / "penv" / "Scripts" / "pio.exe"
    if win_path.exists():
        return str(win_path)
    return "pio"  # Hope for the best


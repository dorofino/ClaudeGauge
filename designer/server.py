class DesignerHandler(http.server.SimpleHTTPRequestHandler):
    def __init__(self, *args, **kwargs):
        super().__init__(*args, directory=str(DESIGNER_DIR), **kwargs)

    def log_message(self, format, *args):
        # Quieter logging — only show API calls
        try:
            path = str(args[0]).split()[1] if args else ""
        except (IndexError, AttributeError):
            return
        if path.startswith("/api/") and not path.startswith("/api/build-status") and not path.startswith("/api/serial-read"):
            super().log_message(format, *args)

    def end_headers(self):
        # Disable caching for all static files so browser always gets latest version
        self.send_header('Cache-Control', 'no-cache, no-store, must-revalidate')
        self.send_header('Pragma', 'no-cache')
        self.send_header('Expires', '0')
        super().end_headers()

    def do_GET(self):
        parsed = urlparse(self.path)

        if parsed.path == "/api/layouts":
            self._serve_layouts()
        elif parsed.path == "/api/build-status":
            self._serve_build_status(parsed)
        elif parsed.path == "/api/serial-ports":
            self._serve_serial_ports()
        elif parsed.path == "/api/serial-read":
            self._serve_serial_read(parsed)
        else:
            super().do_GET()

    def do_POST(self):
        parsed = urlparse(self.path)
        body = self._read_body()

        if parsed.path == "/api/save":
            self._handle_save(body)
        elif parsed.path == "/api/build":
            self._handle_build(body)
        elif parsed.path == "/api/upload":
            self._handle_upload(body)
        elif parsed.path == "/api/serial-connect":
            self._handle_serial_connect(body)
        elif parsed.path == "/api/serial-disconnect":
            self._handle_serial_disconnect()
        else:
            self.send_error(404)

    # ---- API handlers ----

    def _serve_serial_ports(self):
        """List available serial/COM ports."""
        ports = []
        try:
            from serial.tools.list_ports import comports
            for p in comports():
                ports.append({
                    "port": p.device,
                    "desc": p.description or p.device,
                    "hwid": p.hwid or ""
                })
        except ImportError:
            # pyserial not installed — try PlatformIO's device list
            try:
                result = subprocess.run(
                    [PIO_CMD, "device", "list", "--json-output"],
                    capture_output=True, text=True, timeout=10
                )
                if result.returncode == 0:
                    devices = json.loads(result.stdout)
                    for d in devices:
                        ports.append({
                            "port": d.get("port", ""),
                            "desc": d.get("description", d.get("port", "")),
                            "hwid": d.get("hwid", "")
                        })
            except Exception:
                pass
        self._send_json({"ports": ports})

    def _serve_layouts(self):
        if LAYOUTS_FILE.exists():
            data = LAYOUTS_FILE.read_text(encoding="utf-8")
            self._send_json_str(data)
        else:
            self._send_json({"screens": []})

    def _serve_build_status(self, parsed):
        params = parse_qs(parsed.query)
        build_id = params.get("id", [""])[0]
        from_line = int(params.get("from", ["0"])[0])
        status = build_manager.get_status(build_id, from_line)
        self._send_json(status)

    def _handle_save(self, body):
        try:
            data = json.loads(body)
            with open(LAYOUTS_FILE, "w", encoding="utf-8") as f:
                json.dump(data, f, indent=2)
            print(f"  Saved layouts.json ({len(data.get('screens', []))} screens)")
            self._send_json({"ok": True})
        except Exception as e:
            self._send_json({"ok": False, "error": str(e)}, status=400)

    def _handle_build(self, body):
        try:
            data = json.loads(body)
            board = data.get("board", "tdisplay")
            firmware = data.get("firmware", "v1")
            env = ENV_MAP.get(board, "tdisplays3")

            if firmware == "v1":
                # v1: Generate header from saved layouts, then build
                if not LAYOUTS_FILE.exists():
                    self._send_json({"error": "No layouts.json found. Save first."}, status=400)
                    return

                layouts = json.loads(LAYOUTS_FILE.read_text(encoding="utf-8"))
                # Extract v1 screens from dual-format or legacy format
                v1_data = layouts.get("v1", layouts)
                header_content = generate_header(v1_data)
                HEADER_FILE.parent.mkdir(parents=True, exist_ok=True)
                HEADER_FILE.write_text(header_content, encoding="utf-8")
                print(f"  Generated {HEADER_FILE.name}")

                build_id = build_manager.start_build(env, project_dir=FIRMWARE_V1_DIR)
            else:
                # v2: Generate header from saved v2 layouts, then build firmware-v2/
                if not LAYOUTS_FILE.exists():
                    self._send_json({"error": "No layouts.json found. Save first."}, status=400)
                    return

                layouts = json.loads(LAYOUTS_FILE.read_text(encoding="utf-8"))
                v2_data = layouts.get("v2", {})
                if v2_data.get("screens"):
                    header_content = generate_header_v2(v2_data)
                    HEADER_V2_FILE.parent.mkdir(parents=True, exist_ok=True)
                    HEADER_V2_FILE.write_text(header_content, encoding="utf-8")
                    print(f"  Generated v2 {HEADER_V2_FILE.name}")

                build_id = build_manager.start_build(env, project_dir=FIRMWARE_V2_DIR)

            print(f"  Build started (id={build_id}, env={env}, fw={firmware})")
            self._send_json({"build_id": build_id}, status=202)
        except Exception as e:
            self._send_json({"error": str(e)}, status=500)

    def _handle_upload(self, body):
        try:
            data = json.loads(body)
            board = data.get("board", "tdisplay")
            port = data.get("port", "")
            firmware = data.get("firmware", "v1")
            env = ENV_MAP.get(board, "tdisplays3")

            if firmware == "v1":
                # Generate header first (same as build)
                if LAYOUTS_FILE.exists():
                    layouts = json.loads(LAYOUTS_FILE.read_text(encoding="utf-8"))
                    v1_data = layouts.get("v1", layouts)
                    header_content = generate_header(v1_data)
                    HEADER_FILE.parent.mkdir(parents=True, exist_ok=True)
                    HEADER_FILE.write_text(header_content, encoding="utf-8")

                project_dir = FIRMWARE_V1_DIR
            else:
                # v2: Generate header first (same as build)
                if LAYOUTS_FILE.exists():
                    layouts = json.loads(LAYOUTS_FILE.read_text(encoding="utf-8"))
                    v2_data = layouts.get("v2", {})
                    if v2_data.get("screens"):
                        header_content = generate_header_v2(v2_data)
                        HEADER_V2_FILE.parent.mkdir(parents=True, exist_ok=True)
                        HEADER_V2_FILE.write_text(header_content, encoding="utf-8")

                project_dir = FIRMWARE_V2_DIR

            build_id = build_manager.start_build(env, project_dir=project_dir, upload=True, port=port)
            port_info = f", port={port}" if port else ""
            print(f"  Upload started (id={build_id}, env={env}, fw={firmware}{port_info})")
            self._send_json({"build_id": build_id}, status=202)
        except Exception as e:
            self._send_json({"error": str(e)}, status=500)

    # ---- Serial monitor handlers ----

    def _handle_serial_connect(self, body):
        try:
            data = json.loads(body)
            port = data.get("port", "")
            baud = data.get("baud", 115200)
            if not port:
                self._send_json({"error": "No port specified"}, status=400)
                return
            ok = serial_monitor.connect(port, baud)
            if ok:
                self._send_json({"ok": True, "port": port})
            else:
                # Return the last error line from the monitor's buffer
                lines = serial_monitor.get_lines()["lines"]
                err = lines[-1] if lines else "Connection failed"
                self._send_json({"ok": False, "error": err})
        except Exception as e:
            self._send_json({"error": str(e)}, status=500)

    def _handle_serial_disconnect(self):
        serial_monitor.disconnect()
        self._send_json({"ok": True})

    def _serve_serial_read(self, parsed):
        params = parse_qs(parsed.query)
        from_line = int(params.get("from", ["0"])[0])
        status = serial_monitor.get_lines(from_line)
        self._send_json(status)

    # ---- Helpers ----

    def _read_body(self) -> bytes:
        length = int(self.headers.get("Content-Length", 0))
        return self.rfile.read(length)

    def _send_json(self, data: dict, status: int = 200):
        body = json.dumps(data).encode("utf-8")
        self.send_response(status)
        self.send_header("Content-Type", "application/json")
        self.send_header("Content-Length", str(len(body)))
        self.end_headers()
        self.wfile.write(body)

    def _send_json_str(self, json_str: str, status: int = 200):
        body = json_str.encode("utf-8")
        self.send_response(status)
        self.send_header("Content-Type", "application/json")
        self.send_header("Content-Length", str(len(body)))
        self.end_headers()
        self.wfile.write(body)



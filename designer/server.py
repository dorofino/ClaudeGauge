def generate_header(layouts: dict) -> str:
    """Generate screen_layouts.h from saved layout positions."""
    lines = [
        "// Auto-generated by LCARS Designer - DO NOT EDIT",
        "// Re-generate by saving layout in the designer",
        "#pragma once",
        "",
    ]

    # Merge customElements into screens for export
    custom_by_screen = {}
    for ce in layouts.get("customElements", []):
        si = ce.get("screen", -1)
        if si >= 0 and ce.get("elements"):
            custom_by_screen[si] = ce["elements"]

    for si, screen in enumerate(layouts.get("screens", [])):
        screen_name = screen.get("name", "Unknown")
        lines.append(f"// ---- {screen_name} ----")

        # Track sequences for loop elements (e.g., cost_model_0, cost_model_1)
        sequences = {}  # base_id -> [(index, element)]

        all_els = list(screen.get("elements", []))
        all_els.extend(custom_by_screen.get(si, []))

        for el in all_els:
            eid = el.get("id", "")
            if not eid or eid.startswith("screen_title"):
                continue
            if eid.startswith("frame_"):
                continue  # frame elements handled separately below

            # Check if this is a sequence element (ends with _N)
            seq_match = re.match(r'^(.+)_(\d+)$', eid)
            if seq_match:
                base_id = seq_match.group(1)
                idx = int(seq_match.group(2))
                if base_id not in sequences:
                    sequences[base_id] = []
                sequences[base_id].append((idx, el))

            define_name = eid.upper()
            lines.append(f"#define {define_name}_X  {el['x']}")
            lines.append(f"#define {define_name}_Y  {el['y']}")
            if "w" in el:
                lines.append(f"#define {define_name}_W  {el['w']}")
            elif "r" in el:
                # Donut gauges: export diameter as W, applying scale if present
                scale = el.get("scale", 1)
                r_scaled = int(el['r'] * scale)
                lines.append(f"#define {define_name}_W  {r_scaled * 2}")
                # Export scaled thickness for donut gauges
                if el.get("type") == "donutGauge":
                    thk = el.get("thickness", 7)
                    lines.append(f"#define {define_name}_T  {max(2, int(thk * scale))}")
            elif el.get("type") == "donutGauge":
                # Default donut radius is 38
                scale = el.get("scale", 1)
                lines.append(f"#define {define_name}_W  {int(38 * scale) * 2}")
                thk = el.get("thickness", 7)
                lines.append(f"#define {define_name}_T  {max(2, int(thk * scale))}")
            if "h" in el:
                lines.append(f"#define {define_name}_H  {el['h']}")
            # Text element font/color (for standalone labels like countdown text)
            if el.get("type") == "text" and "font" in el:
                font_map = {
                    "builtin": "1", "sm": "LCARS_SM", "14": "LCARS_14", "16": "LCARS_16",
                    "md": "LCARS_MD", "20": "LCARS_20", "22": "LCARS_22", "24": "LCARS_24",
                    "26": "LCARS_26", "lg": "LCARS_LG", "xl": "LCARS_XL",
                }
                fw_font = font_map.get(el["font"], "LCARS_SM")
                lines.append(f"#define {define_name}_FONT  {fw_font}")
            if el.get("type") == "text" and "color" in el:
                lines.append(f"#define {define_name}_COLOR  {el['color']}")
            if el.get("type") == "text" and "datum" in el:
                datum_map = {
                    "TL": "TL_DATUM", "TC": "TC_DATUM", "TR": "TR_DATUM",
                    "MC": "MC_DATUM", "ML": "ML_DATUM", "MR": "MR_DATUM",
                }
                fw_datum = datum_map.get(el["datum"], "TL_DATUM")
                lines.append(f"#define {define_name}_DATUM  {fw_datum}")
            # Donut gauge pct font/color
            if el.get("type") == "donutGauge":
                pct_font = el.get("pctFont", "md")
                pct_color = el.get("pctColor", "CLR_TEXT_BRIGHT")
                pct_font_map = {
                    "builtin": "1", "sm": "LCARS_SM", "14": "LCARS_14", "16": "LCARS_16",
                    "md": "LCARS_MD", "20": "LCARS_20", "22": "LCARS_22", "24": "LCARS_24",
                    "26": "LCARS_26", "lg": "LCARS_LG", "xl": "LCARS_XL",
                }
                fw_pct_font = pct_font_map.get(pct_font, "LCARS_MD")
                lines.append(f"#define {define_name}_PCT_FONT  {fw_pct_font}")
                lines.append(f"#define {define_name}_PCT_COLOR  {pct_color}")

        # Emit START_Y and ROW_STEP for detected sequences
        for base_id, items in sequences.items():
            if len(items) < 2:
                continue
            items.sort(key=lambda x: x[0])
            start_y = items[0][1]["y"]
            step = items[1][1]["y"] - items[0][1]["y"]
            if step > 0:
                define_base = base_id.upper()
                lines.append(f"#define {define_base}_START_Y  {start_y}")
                lines.append(f"#define {define_base}_ROW_STEP  {step}")

        lines.append("")

    # Generate frame element defines (shared across all screens)
    # Search all screens for frame elements, use first occurrence of each
    frame_by_id = {}
    for screen in layouts.get("screens", []):
        for e in screen.get("elements", []):
            eid = e.get("id", "")
            if eid.startswith("frame_") and eid not in frame_by_id:
                frame_by_id[eid] = e
    # Ensure essential frame elements have defaults even if deleted from all screens
    scr_w = 320  # default T-Display-S3
    board = layouts.get("board", "tdisplay")
    if board == "waveshare147":
        scr_w = 172
    essential_defaults = {
        "frame_claude": {"id": "frame_claude", "x": scr_w - 6, "y": 8, "font": "14", "color": "CLR_BG", "text": "CLAUDE.AI"},
        "frame_signal_text": {"id": "frame_signal_text", "x": scr_w - 148, "y": scr_w == 320 and 161 or 158, "font": "builtin", "color": "CLR_TEXT_DIM", "text": "SIGNAL"},
        "frame_signal_bars": {"id": "frame_signal_bars", "x": scr_w - 144, "y": scr_w == 320 and 156 or 153},
        "frame_countdown": {"id": "frame_countdown", "x": scr_w - 120, "y": scr_w == 320 and 155 or 152, "w": 120, "h": 14, "color": "CLR_AMBER"},
    }
    for eid, defaults in essential_defaults.items():
        if eid not in frame_by_id:
            frame_by_id[eid] = defaults
    frame_els = list(frame_by_id.values())
    if frame_els:
        lines.append("// ---- Frame Chrome ----")
        for el in frame_els:
            eid = el["id"]
            define_name = eid.upper()
            lines.append(f"#define {define_name}_X  {el['x']}")
            lines.append(f"#define {define_name}_Y  {el['y']}")
            if "w" in el:
                lines.append(f"#define {define_name}_W  {el['w']}")
            if "h" in el:
                lines.append(f"#define {define_name}_H  {el['h']}")
            if "color" in el:
                lines.append(f"#define {define_name}_COLOR  {el['color']}")
            if "font" in el:
                # Map designer font keys to firmware defines
                font_map = {
                    "builtin": "1", "sm": "LCARS_SM", "14": "LCARS_14", "16": "LCARS_16",
                    "md": "LCARS_MD", "20": "LCARS_20", "22": "LCARS_22", "24": "LCARS_24",
                    "26": "LCARS_26", "lg": "LCARS_LG", "xl": "LCARS_XL",
                }
                fw_font = font_map.get(el["font"], "LCARS_SM")
                lines.append(f"#define {define_name}_FONT  {fw_font}")
            if "text" in el:
                lines.append(f'#define {define_name}_TEXT  "{el["text"]}"')
        lines.append("")

    # Generate custom element arrays per screen
    # Maps designer font/color keys to firmware identifiers
    font_map = {
        "builtin": "nullptr", "sm": "LCARS_SM", "14": "LCARS_14", "16": "LCARS_16",
        "md": "LCARS_MD", "20": "LCARS_20", "22": "LCARS_22", "24": "LCARS_24",
        "26": "LCARS_26", "lg": "LCARS_LG", "xl": "LCARS_XL",
    }
    has_custom = any(custom_by_screen.get(i) for i in range(len(layouts.get("screens", []))))
    if has_custom:
        lines.append("// ---- Custom Elements ----")
        lines.append("#include <cstdint>")
        lines.append("struct CustomTextEl { int16_t x; int16_t y; const char* text; const uint8_t* font; uint16_t color; };")
        lines.append("")

        screen_names = layouts.get("screens", [])
        for si in range(len(screen_names)):
            els = custom_by_screen.get(si, [])
            text_els = [e for e in els if e.get("type") in ("text", "sectionHeader")]
            arr_name = f"CUSTOM_SCREEN_{si}"
            if text_els:
                lines.append(f"static const CustomTextEl {arr_name}[] = {{")
                for el in text_els:
                    x = el.get("x", 0)
                    y = el.get("y", 0)
                    text = el.get("text", "").replace('"', '\\"')
                    font = font_map.get(el.get("font", "sm"), "LCARS_SM")
                    color = el.get("color", "CLR_AMBER")
                    lines.append(f'    {{ {x}, {y}, "{text}", {font}, {color} }},')
                lines.append("};")
                lines.append(f"#define {arr_name}_COUNT  {len(text_els)}")
            else:
                lines.append(f"#define {arr_name}_COUNT  0")
            lines.append("")

    return "\n".join(lines) + "\n"


def generate_header_v2(layouts: dict) -> str:
    """Generate screen_layouts.h for firmware-v2 (lcars-esp32 engine).

    Maps designer element IDs to #define macros with positions, sizes, and
    font/datum values using lcars-esp32's LcarsFontSize enum names.
    """
    # lcars-esp32 uses LCARS_FONT_* enum values (not v1's LCARS_SM shorthand)
    v2_font_map = {
        "builtin": "1",
        "sm": "LCARS_FONT_SM", "14": "LCARS_FONT_14", "16": "LCARS_FONT_16",
        "md": "LCARS_FONT_MD", "20": "LCARS_FONT_20",
        "22": "LCARS_FONT_20",   # no 22px in engine, use closest
        "24": "LCARS_FONT_LG",   # no 24px in engine, use closest (28px)
        "26": "LCARS_FONT_LG",   # no 26px in engine, use closest (28px)
        "lg": "LCARS_FONT_LG", "xl": "LCARS_FONT_XL",
    }
    datum_map = {
        "TL": "TL_DATUM", "TC": "TC_DATUM", "TR": "TR_DATUM",
        "MC": "MC_DATUM", "ML": "ML_DATUM", "MR": "MR_DATUM",
    }
    # Map designer CLR_ names to lcars-esp32 LCARS_ color constants
    color_map = {
        "CLR_BG": "LCARS_BLACK",
        "CLR_AMBER": "LCARS_AMBER",
        "CLR_SALMON": "LCARS_TOMATO",
        "CLR_PEACH": "LCARS_PEACH",
        "CLR_LAVENDER": "LCARS_LAVENDER",
        "CLR_BLUE": "LCARS_PERIWINKLE",
        "CLR_ORANGE": "LCARS_ORANGE",
        "CLR_RED": "LCARS_MARS",
        "CLR_SKY": "LCARS_ICE",
        "CLR_TAN": "LCARS_TAN",
        "CLR_TEXT_BRIGHT": "LCARS_WHITE",
        "CLR_TEXT_DIM": "LCARS_TEXT_DIM",
        "CLR_BAR_BG": "LCARS_BAR_TRACK",
        "CLR_STATUS_OK": "LCARS_ICE",
        "CLR_STATUS_ERR": "LCARS_TOMATO",
        "CLR_ICE": "LCARS_ICE",
    }

    def map_color(c):
        return color_map.get(c, c)

    lines = [
        "// Auto-generated by LCARS Designer for firmware-v2 — DO NOT EDIT",
        "// Re-generate by saving/building in the designer",
        "#pragma once",
        "",
    ]

    for si, screen in enumerate(layouts.get("screens", [])):
        screen_name = screen.get("name", "Unknown")
        lines.append(f"// ---- Screen {si}: {screen_name} ----")

        for el in screen.get("elements", []):
            eid = el.get("id", "")
            if not eid or eid.startswith("screen_title"):
                continue
            if eid.startswith("frame_"):
                continue  # frame chrome handled below

            define_name = eid.upper()
            el_type = el.get("type", "")

            # Position (all elements)
            lines.append(f"#define {define_name}_X  {el['x']}")
            lines.append(f"#define {define_name}_Y  {el['y']}")

            # Donut gauge: emit diameter (W), thickness (T), pct font/color
            if el_type == "donutGauge":
                scale = el.get("scale", 1)
                r = el.get("r", 38)
                r_scaled = int(r * scale)
                thk = el.get("thickness", 7)
                thk_scaled = max(2, int(thk * scale))
                lines.append(f"#define {define_name}_R  {r_scaled}")
                lines.append(f"#define {define_name}_T  {thk_scaled}")
                pct_font = el.get("pctFont", "md")
                pct_color = el.get("pctColor", "CLR_TEXT_BRIGHT")
                lines.append(f"#define {define_name}_PCT_FONT  {v2_font_map.get(pct_font, 'LCARS_FONT_MD')}")
                lines.append(f"#define {define_name}_PCT_COLOR  {map_color(pct_color)}")
                if "color" in el:
                    lines.append(f"#define {define_name}_COLOR  {map_color(el['color'])}")

            # Size (w/h)
            if "w" in el:
                lines.append(f"#define {define_name}_W  {el['w']}")
            if "h" in el:
                lines.append(f"#define {define_name}_H  {el['h']}")

            # Text element: font, color, datum
            if el_type == "text":
                if "font" in el:
                    lines.append(f"#define {define_name}_FONT  {v2_font_map.get(el['font'], 'LCARS_FONT_SM')}")
                if "color" in el:
                    lines.append(f"#define {define_name}_COLOR  {map_color(el['color'])}")
                if "datum" in el:
                    lines.append(f"#define {define_name}_DATUM  {datum_map.get(el['datum'], 'TL_DATUM')}")

            # vline/hline color
            if el_type in ("vline", "hline") and "color" in el:
                lines.append(f"#define {define_name}_COLOR  {map_color(el['color'])}")

            # countdownBar / statusRow color
            if el_type in ("countdownBar", "statusRow") and "color" in el:
                lines.append(f"#define {define_name}_COLOR  {map_color(el['color'])}")

        lines.append("")

    # Frame chrome defines (shared across screens, use first occurrence)
    frame_by_id = {}
    for screen in layouts.get("screens", []):
        for e in screen.get("elements", []):
            eid = e.get("id", "")
            if eid.startswith("frame_") and eid not in frame_by_id:
                frame_by_id[eid] = e

    if frame_by_id:
        lines.append("// ---- Frame Chrome ----")
        for el in frame_by_id.values():
            eid = el["id"]
            define_name = eid.upper()
            lines.append(f"#define {define_name}_X  {el['x']}")
            lines.append(f"#define {define_name}_Y  {el['y']}")
            if "w" in el:
                lines.append(f"#define {define_name}_W  {el['w']}")
            if "h" in el:
                lines.append(f"#define {define_name}_H  {el['h']}")
            if "color" in el:
                lines.append(f"#define {define_name}_COLOR  {map_color(el['color'])}")
            if "font" in el:
                lines.append(f"#define {define_name}_FONT  {v2_font_map.get(el['font'], 'LCARS_FONT_SM')}")
            if "text" in el:
                lines.append(f'#define {define_name}_TEXT  "{el["text"]}"')
        lines.append("")

    return "\n".join(lines) + "\n"



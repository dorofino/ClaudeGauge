class BuildManager:
    """Manages background builds with output buffering."""

    def __init__(self):
        self._builds = {}
        self._next_id = 0
        self._lock = threading.Lock()

    def start_build(self, env: str, project_dir: Path = None, upload: bool = False, port: str = "") -> str:
        with self._lock:
            build_id = str(self._next_id)
            self._next_id += 1

        fw_dir = project_dir or FIRMWARE_V1_DIR
        cmd = [PIO_CMD, "run", "-d", str(fw_dir), "-e", env]
        if upload:
            cmd += ["--target", "upload"]
            if port:
                cmd += ["--upload-port", port]

        try:
            proc = subprocess.Popen(
                cmd, stdout=subprocess.PIPE, stderr=subprocess.STDOUT,
                text=True, bufsize=1
            )
        except FileNotFoundError:
            self._builds[build_id] = {
                "process": None,
                "lines": [f"ERROR: PlatformIO not found at: {PIO_CMD}",
                          "Install PlatformIO or check your PATH."],
                "done": True,
                "success": False
            }
            return build_id

        build = {
            "process": proc,
            "lines": [f"$ {' '.join(cmd)}"],
            "done": False,
            "success": False
        }
        self._builds[build_id] = build

        thread = threading.Thread(target=self._reader, args=(build_id,), daemon=True)
        thread.start()

        return build_id

    def _reader(self, build_id: str):
        build = self._builds[build_id]
        proc = build["process"]
        try:
            for line in proc.stdout:
                build["lines"].append(line.rstrip())
            proc.wait()
            build["success"] = (proc.returncode == 0)
            build["lines"].append(
                f"\n{'BUILD SUCCESS' if build['success'] else 'BUILD FAILED'} (exit code {proc.returncode})"
            )
        except Exception as e:
            build["lines"].append(f"ERROR: {e}")
            build["success"] = False
        finally:
            build["done"] = True

    def get_status(self, build_id: str, from_line: int = 0) -> dict:
        build = self._builds.get(build_id)
        if not build:
            return {"error": "Unknown build ID", "done": True, "success": False, "lines": []}
        return {
            "lines": build["lines"][from_line:],
            "done": build["done"],
            "success": build["success"]
        }


